    private void jBRetornaIndiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRetornaIndiceActionPerformed
        int acha = Integer.parseInt(JOptionPane.showInputDialog(this, "Qual número deseja buscar a posição?", "App", 3));
        int posicoes[] = colecao.posicao(acha);
        if (posicoes != null) {
            jTAResultado.setText(colecao.vetorToString(posicoes, "O valor " + acha + " foi localizado em: "));
        } else {
            jTAResultado.setText("O valor " + acha + " não foi localizado na coleção! ");
        }


    }//GEN-LAST:event_jBRetornaIndiceActionPerformed

