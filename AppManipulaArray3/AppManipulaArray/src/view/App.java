    private void jBSubstituiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSubstituiActionPerformed
        int localiza = Integer.parseInt(JOptionPane.showInputDialog(this, "Informe o número que deseja substituir?", "App", 0));
        colecao.substitui(localiza);

        if (colecao.substitui(localiza) == false) {
            JOptionPane.showMessageDialog(this, "Nenhum valor encontrado", "Aviso", 0);
        } else {
            int substituto = Integer.parseInt(JOptionPane.showInputDialog(this, "Informe o novo número?", "App", 0));
            colecao.substituir(localiza, substituto);

            jTAResultado.setText(colecao.dados());
        }
    }//GEN-LAST:event_jBSubstituiActionPerformed

    private void jBBuscaBinariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscaBinariaActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog(this, "Qual número deseja buscar?", "App", 3));
        colecao.buscaBinaria(x);
        if (colecao.buscaBinaria(x) != -1) {
            jTAResultado.setText(colecao.dados() + "\n O número: " + x + " foi encontrado");

        } else {
            JOptionPane.showMessageDialog(this, "Não encontramos o número : " + x, "", 1);
        }

    }//GEN-LAST:event_jBBuscaBinariaActionPerformed

