    private void jBDivisivelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDivisivelActionPerformed
        int divisor = Integer.parseInt(JOptionPane.showInputDialog(this, "Qual número deseja buscar a posição?", "App", 3));
        int divide[] = colecao.dividir(divisor);
        if (divide != null) {
            jTAResultado.setText(colecao.dados() + colecao.divididosToString(divide, "\nOs números divisíveis por: " + divisor + " são: "));
        } else {
            jTAResultado.setText("Não existe números divisíveis por: " + divisor);
        }


    }//GEN-LAST:event_jBDivisivelActionPerformed

