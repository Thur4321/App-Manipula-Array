    private void jBAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarActionPerformed
        if (jTFNumeros.getText().isEmpty()) {
            JOptionPane.showInputDialog(this, "Informe o número a ser adicionado", "Aviso", 0);
            jTFNumeros.requestFocusInWindow();
            jTFNumeros.setText(null);
            return;
        }

        if (!colecao.adiciona(Integer.parseInt(jTFNumeros.getText()))) {
            JOptionPane.showMessageDialog(this, "Não foi possível adicionar. Coleção Completa", "Aviso", 0);
            return;
        }

        jTAResultado.setText(colecao.dados());
        jTFNumeros.setText(null);
        jTFNumeros.requestFocusInWindow();
    }//GEN-LAST:event_jBAdicionarActionPerformed

