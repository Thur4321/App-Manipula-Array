    private void jTFNumerosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFNumerosKeyTyped
        char tecla = evt.getKeyChar();
        if (!Character.isDigit(tecla) && tecla != '.') {
            this.getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTFNumerosKeyTyped

